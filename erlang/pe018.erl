start() ->
  pe018(lists:reverse(data())).
  
pe018([H,H1|T]) ->
  Out = row_max(H, []),
  Sum = add_rows(Out, H1, []),
  pe018([Sum | T]);

pe018([H]) ->
  io:format("~w~n", H).
    
add_rows([], [], Last) ->
  lists:reverse(Last);

add_rows([H1|T1], [H2|T2], Last) ->
  add_rows(T1, T2, [H1+H2|Last]).

row_max([H,H1|T], Out) ->
  NewOut = [ max(H, H1) | Out],
  row_max([H1|T], NewOut);

row_max([_H|_T], Last) ->
  lists:reverse(Last).
